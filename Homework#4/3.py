import re

def normalize_phone(phone_number: str) -> str:
    # 1. –í–∏–¥–∞–ª—è—î–º–æ –∑–∞–π–≤—ñ –ø—Ä–æ–±—ñ–ª–∏ –Ω–∞ –ø–æ—á–∞—Ç–∫—É —Ç–∞ –≤ –∫—ñ–Ω—Ü—ñ
    phone_number = phone_number.strip()

    # 2. –í–∏–¥–∞–ª—è—î–º–æ –≤—Å—ñ —Å–∏–º–≤–æ–ª–∏, –∫—Ä—ñ–º —Ü–∏—Ñ—Ä —Ç–∞ '+'
    phone_number = re.sub(r"[^\d+]", "", phone_number)

    # 3. –§–æ—Ä–º–∞—Ç—É—î–º–æ –Ω–æ–º–µ—Ä –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω–æ –¥–æ –ø—Ä–∞–≤–∏–ª
    if phone_number.startswith("+"):
        # –Ø–∫—â–æ –Ω–æ–º–µ—Ä –≤–∂–µ –ø–æ—á–∏–Ω–∞—î—Ç—å—Å—è –∑ '+', –ø–µ—Ä–µ–≤—ñ—Ä–∏–º–æ —á–∏ —î –∫–æ–¥ –∫—Ä–∞—ó–Ω–∏
        if not phone_number.startswith("+38"):
            # –Ø–∫—â–æ –º—ñ–∂–Ω–∞—Ä–æ–¥–Ω–∏–π –∫–æ–¥ –Ω–µ '+38', –∞–ª–µ –ø–æ—á–∏–Ω–∞—î—Ç—å—Å—è –∑ '380', –¥–æ–¥–∞—î–º–æ '+'
            if phone_number.startswith("+380"):
                return phone_number
            else:
                # –ó–∞–ª–∏—à–∞—î–º–æ —è–∫ —î ‚Äî –∞–±–æ –º–æ–∂–Ω–∞ –¥–æ–¥–∞—Ç–∏ –ª–æ–≥—ñ–∫—É –¥–ª—è —ñ–Ω—à–∏—Ö –∫—Ä–∞—ó–Ω
                return phone_number
        else:
            return phone_number
    elif phone_number.startswith("380"):
        # –Ø–∫—â–æ –Ω–æ–º–µ—Ä –ø–æ—á–∏–Ω–∞—î—Ç—å—Å—è –∑ 380 ‚Äî –¥–æ–¥–∞—î–º–æ '+'
        return "+" + phone_number
    else:
        # –Ø–∫—â–æ –Ω–æ–º–µ—Ä –±–µ–∑ –∫–æ–¥—É ‚Äî –¥–æ–¥–∞—î–º–æ '+38'
        return "+38" + phone_number


# üî∏ –ü—Ä–∏–∫–ª–∞–¥ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è
raw_numbers = [
    "067\t123 4567",
    "(095) 234-5678\n",
    "+380 44 123 4567",
    "380501234567",
    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
]

sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
print("–ù–æ—Ä–º–∞–ª—ñ–∑–æ–≤–∞–Ω—ñ –Ω–æ–º–µ—Ä–∏ —Ç–µ–ª–µ—Ñ–æ–Ω—ñ–≤ –¥–ª—è SMS-—Ä–æ–∑—Å–∏–ª–∫–∏:", sanitized_numbers)